print('Converter Metros para Centimetros e Centimetros para Milimetros')
metros = float(input('Digite os metros que voce quer converter: '))
centimetros = metros * 100
milimetros = metros * 1000
print(f'O valor de metros {metros} convertido para centimetros é {centimetros}, e o valor de metros'
      f' para milimetros é {milimetros}')